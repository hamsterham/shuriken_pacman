void MovePac(void){
  // move pacman
  switch(pac.speed){
    case DIRECTIONL:
      pac.xpos += SPD_LEFT_UP_NORM;
      if(pac.xpos < 0)
        pac.xpos = 0;
      break;
    case DIRECTIONR:
      pac.xpos += SPD_RIGHT_DOWN_NORM;
      if(pac.xpos > SCREENW - PACMANW)
        pac.xpos = SCREENW - PACMANW;
      break;
    case DIRECTIOND:     
      pac.ypos += SPD_RIGHT_DOWN_NORM;
      if(pac.ypos > SCREENH)
        pac.ypos = SCREENH;
      break;
    case DIRECTIONU:
      pac.ypos += SPD_LEFT_UP_NORM;
      if(pac.ypos < PACMANH)
        pac.ypos = PACMANH;
      break;
    case DIRECTIONSTOP:
      break;
  } 
  pac.center_x = pac.xpos + PACMANW/2;
  pac.center_y = pac.ypos - PACMANH/2;
}

